
package model;

public class Direccion {
    private final String calle;
    private final String piso;
    private final String ciudad;
    private final int numero;

    public Direccion(String calle, String piso, String ciudad, int numero) {
        this.calle = calle;
        this.piso = piso;
        this.ciudad = ciudad;
        this.numero = numero;
    } 
}
